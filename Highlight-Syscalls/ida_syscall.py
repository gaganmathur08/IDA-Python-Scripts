def get_file_format():
    file_format = None
    file_format_str = idaapi.get_file_type_name()
    if "ELF" in file_format_str:
        file_format = "ELF"
    elif "Mach-O" in file_format_str:
        file_format = "Mach-O"
    return file_format


# from keypatch
# https://github.com/keystone-engine/keypatch/blob/bfcaef11de3a90efb08ed4f0c39dccf40d5613d0/keypatch.py#L171-L177
def get_arch():
    arch = None
    info = idaapi.get_inf_structure()

    try:
        cpuname = info.procname.lower()
    except Exception:
        cpuname = info.procName.lower()

    if cpuname.startswith("arm"):
        if info.is_64bit():
            arch = "ARM64"
        elif info.is_32bit():
            arch = "ARM32"
        else:
            print("[!] This script only supports ARM architecture")

    return arch

def FindInstructions(instr):
    result = []
    ea = ida_ida.inf_get_min_ea()
    while True:
        ea = ida_search.find_text(ea, 0, 0, instr, idc.SEARCH_REGEX | idc.SEARCH_DOWN)
        if ea == idaapi.BADADDR:
            break
        # print(hex(ea))
        result.append(ea)
        ea = idc.next_head(ea)
    return result


def findSyscallNumber(svc_instr_addr):

    is_elf = False
    is_macho = False
    file_format = get_file_format()

    if "ELF" in file_format:
        is_elf = True
    elif "Mach-O" in file_format:
        is_macho = True

    # iterate back from svc instruction and search for value set in w16
    ea = svc_instr_addr
    instr_count = 0
    while (instr_count != MAX_INSTR):
        ea = idc.prev_head(ea)
        instr = idc.GetDisasm(ea)

        # GetMnem function of IDA is troublesome, do the matching hardway
        # https://reverseengineering.stackexchange.com/questions/16483/extract-conditional-instructions-using-ida-python-in-arm-binaries

        instr = instr.split()
        syscall_num = None
        if "MOV" in instr[0]:
            if is_macho:
                if XNU_SYSCALL_REGISTER in instr[1] or XNU_SYSCALL_REGISTER_2 in instr[1]:
                    syscall_num = instr[2][1:]  # get rid of the '#' before the syscall numbers
                    break

            if is_elf:
                if (ANDROID_SYSCALL_REGISTER in instr[1]) or (ANDROID_SYSCALL_REGISTER_2 in instr[1]):
                    syscall_num = instr[2][1:]  # get rid of the '#' before the syscall numbers
                    break

        # print(hex(ea) + ": " + instr)
        instr_count += 1
    return syscall_num


def is_text_segment(ea):
    if "text" in idc.get_segm_name(ea):
        return True
    else:
        return False


def add_comment(ea, syscall_num, syscall_name):
    comment = "Syscall: " + syscall_name
    idc.set_cmt(ea, comment, 0)


def main():
    # Check for the supported CPU architectures
    arch = get_arch()
    if "ARM64" not in arch:
        print("[!] This script only works for ARM64")
        return

    # Check for the supported File formats
    is_elf = False
    is_macho = False
    file_format = get_file_format()

    if "ELF" in file_format:
        is_elf = True
    elif "Mach-O" in file_format:
        is_macho = True
    else:
        return

    result = FindInstructions("svc")
    for ea in result:
        if is_text_segment:
            syscall_num = findSyscallNumber(ea)
            if syscall_num is not None:
                # syscall number is in hex
                if '0x' in syscall_num:
                    syscall_num = int(syscall_num, 16)

                # For iOS
                if is_macho:
                    try:
                        print(hex(ea) + " " + str(syscall_num) + " " + xnu_syscall[str(syscall_num)])
                        add_comment(ea, syscall_num, xnu_syscall[str(syscall_num)])
                    except Exception:
                        print("ERROR " + hex(ea) + " " + str(syscall_num))

                # ANDROID
                elif is_elf:
                    try:
                        print(hex(ea) + " " + str(syscall_num) + " " + android_syscall[str(syscall_num)])
                        add_comment(ea, syscall_num, android_syscall[str(syscall_num)])
                    except Exception:
                        print("ERROR " + hex(ea) + " " + str(syscall_num))

            else:
                print(hex(ea) + " " + str(syscall_num))


main()
